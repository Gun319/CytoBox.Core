namespace Cytobox.Core.HttpTool
{
    /// <summary>
    /// ContentType 类型
    /// </summary>
    public class HttpContentType
    {
        /// <summary>
        /// HTML格式
        /// </summary>
        public const string TEXT_HTML = "text/html";
        /// <summary>
        /// 纯文本格式
        /// </summary>
        public const string TEXT_PLAIN = "text/plain; charset=utf-8";
        /// <summary>
        /// XML格式
        /// </summary>
        public const string TEXT_XML = "text/plain; charset=utf-8";
        /// <summary>
        /// gif图片格式
        /// </summary>
        public const string IMAGE_GIF = "image/gif";
        /// <summary>
        /// jpg图片格式
        /// </summary>
        public const string IMAGE_JPEG = "image/jpeg";
        /// <summary>
        /// png图片格式
        /// </summary>
        public const string IMAGE_PNG = "image/png";

        /// <summary>
        /// XHTML格式
        /// </summary>
        public const string APPLICATION_XHTML_XML = "application/xhtml+xml";
        /// <summary>
        /// XML数据格式
        /// </summary>
        public const string APPLICATION_XML = "application/xml";
        /// <summary>
        /// Atom XML聚合格式
        /// </summary>
        public const string APPLICATION_ATOM_XML = "application/atom+xml";
        /// <summary>
        /// JSON数据格式
        /// </summary>
        public const string APPLICATION_JSON = "application/json; charset=utf-8";
        /// <summary>
        /// pdf格式
        /// </summary>
        public const string APPLICATION_PDF = "application/pdf";
        /// <summary>
        /// Word文档格式
        /// </summary>
        public const string APPLICATION_MSWORD = "application/msword";
        /// <summary>
        /// 二进制流数据
        /// </summary>
        public const string APPLICATION_OCTET_STREAM = "application/octet-stream";
        /// <summary>
        /// 表单默认的提交数据的格式
        /// </summary>
        public const string APPLICATION_X_WWW_FORM_URLENCODED = "application/x-www-form-urlencoded";
    }
}
